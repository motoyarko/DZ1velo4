public class User {

    private Long age;
    private Long height;
    private Long weight;
    private Long girth;

    public void setAge(Long age) {
        this.age = age;
    }

    public void setHeight(Long height) {
        this.height = height;
    }

    public void setWeight(Long weight) {
        this.weight = weight;
    }

    public void setGirth(Long girth) {
        this.girth = girth;
    }


    public Long getAge() {
        return age;
    }

    public Long getHeight() {
        return height;
    }

    public Long getWeight() {
        return weight;
    }

    public Long getGirth() {
        return girth;
    }
}
